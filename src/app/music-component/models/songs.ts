/**
 * Structure of the songs.
 *
 * @export
 * @class Songs
 */
export class Songs {
    /**
     * Name of the song.
     *
     * @memberof Songs
     */
    public song?: string;

    /**
     * Link of the song.
     *
     * @memberof Songs
     */
    public link?: string;

    /**
     * Name of the artist.
     *
     * @type {string}
     * @memberof Songs
     */
    public artist?: string;
}